	<h1>MARIAN ZWIERZCHOWSKI</h1>
	<h2>My artistic profile:</h2>
	<p>Born in 1944. Graduated from Czartoryski high school in Pulawy / Poland. Wroclaw University of Art and Design, diploma at the Kraków Technical College, art teacher in high school in Pulawy / Poland.</p>
	<p>As a freelance artist exhibits his paintings in Kazimierz on the Vistula, Pulawy, Lublin, Gorzów Wielkopolski, Kraków and Wroclaw, Sucha Beskidzka, Maków Podhalański and Swinoujscie.</p>
	<p>Since 1981 lives and works in the Germany. First in Hessian Führt / Odenwald, then in Heilbronn as a freelance artist and graphic designer. As a member of the Kunstverein Hofgeismar and then a member of the Kunstverein Heilbronn participates in several exhibitions.</p>
	<p>From 1982 to 2005 among others exhibits his work in Kassel, Hofgeismar, Heilbronn, Oedheim, Stuttgart, Augsburg, Bad Nauheim, Cuxhaven, Nordenham, Bergen on Ruegen, Herne, Pfullendorf, Neubrandenburg and Eidhoven/Holland.</p>
	<p>Since 2006 lives and works in Swinoujscie / Poland. He actively collaborates with the art gallery &quot;ART&quot; in Swinoujscie, participates in several exhibitions and artistic workshops in Poland and abroad.</p>
	<p>Member of the Polish Association of Artists and Designers.</p>
	<p>Member of the International Professional Art Association VIPAA, New York, USA</p>
