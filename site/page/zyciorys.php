	<h1>MARIAN ZWIERZCHOWSKI - Życiorys</h1> 
	<p>malarz, grafik, projektant form przemysłowych, nauczyciel i technolog. Różnorodność zawodów, zamiłowań i pasji była odbiciem jego losów życiowych. Do roku 1981 w kraju, jako czynny członek i współzałozyciel Puławskiego Klubu Plastyków i członek Towarzystwa Sztuk Pięknych w Lublinie, potem przez ponad 30 lat w róznych krajach Europy Zachodniej, gdzie pracował twórczo, wystawiał i projektował formy przemysłowe dla potrzeb marketingowych znanych firm kosmetycznych, farmaceutycznych i optycznych.</p>
	<p>Podróżował po Europie, Ameryce Północnej i Środkowej, Afryce Północnej i Azji zbierając doświadczenia, malując, pasjonując się sztuka i kulturą tych kontynentów, odzwiedza Alaskę, Meksyk, Koreę, Japonię, Indonezję, Gruzję, Chiny i Tunezję.</p>
	<p>Obecnie mieszka w Świnoujściu, czynnie działa w świnoujskim srodowisku artystycznym wystawiając, biorąc udział w licznych plenerach krajowych i zagranicznych. </p>
	<p>Aktywnie współpracuję z Galerią „ART” w Świnoujsciu, brał udział w cyklicznych wystawch „KUNST OFFEN” i „KUNST HEUTE” oraz uczestniczył w Programie Współpracy Transgranicznej Południowy Bałtyk 2007-2013 „FOUR CORNERS”. Od roku 2011 bierze udział w corocznych wystawach w Teltow koło Berlina i w cyklicznych międzynarodowech wystawach Kap-Hoorn ART w Bremen i w FormART Glinde koło Hamburga.</p>
	<p>Jest członkiem Związku Polskich Artystów Plastyków Polska Sztuka Użytkowa.</p>
